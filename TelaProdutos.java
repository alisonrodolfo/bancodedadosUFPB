                    jFormattedTextFieldCodigo.setText(dados.getCodigo()); 
                    jFormattedTextFieldValidade.setText(dados.getValidade());
                    jFormattedTextFieldPeso.setText(dados.getPeso());
                    jComboBoxTipo.setSelectedItem(dados.getTipo_pao());
                    String[] reply2 = dados.getMateria_prima().split("@");
                    modelo.clear();
                    jListMateria.setModel(modelo);
                     for (int i = 0; i < reply2.length; i++) {
                         modelo.addElement(reply2[i]);
                      }
                 jListMateria.setModel(modelo);
