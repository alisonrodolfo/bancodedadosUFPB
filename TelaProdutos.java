    public  synchronized Pao Produto(String codigo) throws ClassNotFoundException{
        try {
            dados = new Pao(0,jFormattedTextFieldCodigo.getText(),"","","","","");
            dados = Main.atualizaMysqlProduto(dados, 3);
            jFormattedTextFieldCodigo.setEditable(false);
            if(jFormattedTextFieldCodigo.getText().equals(dados.getCodigo())){
                    jFormattedTextFieldValor.setText(dados.getValor());
                    jFormattedTextFieldCodigo.setText(dados.getCodigo()); 
                    jFormattedTextFieldValidade.setText(dados.getValidade());
                    jFormattedTextFieldPeso.setText(dados.getPeso());
                    jComboBoxTipo.setSelectedItem(dados.getTipo_pao());
                    String[] reply = dados.getMateria_prima().split("@");
                    modelo.clear();
                    jListMateria.setModel(modelo);
                     for (int i = 0; i < reply.length; i++) {
                         modelo.addElement(reply[i]);
                      }
                 jListMateria.setModel(modelo);
                jLabelID.setText("ID do Produto: "+dados.getIdProduto());
                jLabelERROCPF.setText("");
                jFormattedTextFieldCodigo.setEditable(true);
                
            } else {
                jLabelERROCPF.setText("Não Existe Produto com esse Código");
                jLabelID.setText("");
                jFormattedTextFieldValidade.setText(""); jFormattedTextFieldPeso.setText(""); jFormattedTextFieldValor.setText("");
                jFormattedTextFieldCodigo.setEditable(true);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(TelaProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return dados;
    }
    


