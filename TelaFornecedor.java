    public synchronized void atualizarFornecedor() throws ClassNotFoundException{
        int id;
        if((jTextFieldNome.getText().equals("") == false)
            && (jTextFieldEndereco.getText().equals("") == false)    
            && (jFormattedTextFieldTELEFONE.getText().equals("(  )    -    ") == false)
            && (jFormattedTextFieldCodigo.getText().equals("") == false)
            && (jTextFieldContato.getText().equals("") == false)){
            try {
                jFormattedTextFieldCodigo.setEditable(false);
                
                    dados = new Fornecedor(dados.getIdFornc(),jFormattedTextFieldCodigo.getText(),jTextFieldNome.getText(),
                            jTextFieldEndereco.getText(),jFormattedTextFieldTELEFONE.getText(),jTextFieldContato.getText());
                    Main.atualizaMysqlFornecedor(dados, 2);
                    jTextFieldNome.setText(dados.getNome());
                    jFormattedTextFieldTELEFONE.setText(dados.getTelefone());
                    jTextFieldEndereco.setText(dados.getEndereco());
                    jFormattedTextFieldCodigo.setText(dados.getCGC()); 
                    jTextFieldContato.setText(dados.getContato()); 
                    jLabelID.setText("ID do Fornecedor: "+dados.getIdFornc());
                    jLabelERROCPF.setText("");
                    jFormattedTextFieldCodigo.setEditable(true);
                 
            } catch (SQLException ex) {
                Logger.getLogger(TelaFornecedor.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                 excluir = false; 
            }
        }
    }
    
